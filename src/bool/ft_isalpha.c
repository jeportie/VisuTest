#include "../../include/libft.h"

/*
 * Check if the char is alphabetic
 */

bool	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (true);
	return (false);
}
